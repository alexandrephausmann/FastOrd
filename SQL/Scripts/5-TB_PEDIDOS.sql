CREATE TABLE TB_PEDIDO (
	COD_PEDIDO INT IDENTITY(1,1),
	RETIRADA VARCHAR(1),
	COD_TIPO_INTEGRACAO INT,
	COD_STATUS_PEDIDO INT,
	RUA VARCHAR(255),
	BAIRRO VARCHAR(255),
	CEP VARCHAR(15),
	NUM_RESIDENCIA INT,
	DADO_COMPLEMENTAR VARCHAR(255),
	NUM_CELULAR VARCHAR(15),
	PRIMARY KEY (COD_PEDIDO),
	FOREIGN KEY (COD_TIPO_INTEGRACAO) REFERENCES TB_TIPO_INTEGRACAO(COD_TIPO_INTEGRACAO),
	FOREIGN KEY (COD_STATUS_PEDIDO) REFERENCES TB_STATUS_PEDIDO(COD_STATUS_PEDIDO)
);

