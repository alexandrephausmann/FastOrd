CREATE TABLE TB_ORDER (
	ID_ORDER INT IDENTITY(1,1),
	WITHDRAWAL VARCHAR(1),
	ID_INTEGRATION_TYPE INT,
	ID_ORDER_STATUS INT,
	ROAD VARCHAR(255),
	DISTRICT VARCHAR(255),
	ZIP_CODE VARCHAR(15),
	HOUSE_NUMBER INT,
	COMPLEMENTARY_DATA VARCHAR(255),
	MOBILE_NUMBER VARCHAR(15),
	PRIMARY KEY (ID_ORDER),
	FOREIGN KEY (ID_ORDER_STATUS) REFERENCES TB_ORDER_STATUS(ID_ORDER_STATUS)
);

